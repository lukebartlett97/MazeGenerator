package mazes.savedPatterns;

import mazes.Maze;
import mazes.templates.*;

import java.util.ArrayList;
import java.util.List;

public class Smile {

    public static List<MazeTemplate> getTemplates(int size)
    {
        List<MazeTemplate> templates = new ArrayList<>();
        WallTemplate wallTemplate = new WallTemplate();
        CircleTemplate botMouth = new CircleTemplate(size/2, (size + 6)/2, size/3);
        CircleTemplate topMouth = new CircleTemplate(size/2, (size - 6) /2, size/3, Maze.OPEN);
        CircleTemplate leftEye = new CircleTemplate(size/4, size/4, size/8);
        CircleTemplate rightEye = new CircleTemplate(size*3/4, size/4, size/8);
        templates.add(wallTemplate);
        templates.add(botMouth);
        templates.add(topMouth);
        templates.add(leftEye);
        templates.add(rightEye);
        return templates;
    }
}


