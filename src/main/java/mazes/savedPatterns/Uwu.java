package mazes.savedPatterns;

import mazes.Maze;
import mazes.templates.CircleTemplate;
import mazes.templates.MazeTemplate;
import mazes.templates.RectangleTemplate;
import mazes.templates.WallTemplate;

import java.util.ArrayList;
import java.util.List;

public class Uwu {

    public static List<MazeTemplate> getTemplates()
    {
        List<MazeTemplate> templates = new ArrayList<>();
        WallTemplate wallTemplate = new WallTemplate();
        CircleTemplate botMouthleft = new CircleTemplate(23, 38, 8);
        CircleTemplate topMouthleft = new CircleTemplate(24, 34, 8, Maze.OPEN);
        CircleTemplate botMouthright = new CircleTemplate(37, 38, 8);
        CircleTemplate topMouthright = new CircleTemplate(36, 34, 8, Maze.OPEN);
        CircleTemplate leftEye = new CircleTemplate(15, 15, 8);
        RectangleTemplate leftEyeTop = new RectangleTemplate(5, 5, 25, 11, Maze.OPEN);
        CircleTemplate leftEyeGap = new CircleTemplate(15, 11, 3, Maze.OPEN);
        CircleTemplate rightEye = new CircleTemplate(45, 15, 8);
        RectangleTemplate rightEyeTop = new RectangleTemplate(35, 5, 55, 11, Maze.OPEN);
        CircleTemplate rightEyeGap = new CircleTemplate(45, 11, 3, Maze.OPEN);
        templates.add(wallTemplate);
        templates.add(botMouthleft);
        templates.add(topMouthleft);
        templates.add(botMouthright);
        templates.add(topMouthright);
        templates.add(leftEye);
        templates.add(rightEye);
        templates.add(leftEyeTop);
        templates.add(rightEyeTop);
        templates.add(leftEyeGap);
        templates.add(rightEyeGap);
        return templates;
    }
}

